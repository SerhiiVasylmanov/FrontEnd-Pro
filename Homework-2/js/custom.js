var studentAge = 29;
var teacherAge = 32;


if (studentAge === teacherAge) {
    alert(`numbers are equal (${studentAge} = ${teacherAge})`);
} else if (studentAge > teacherAge) {
    alert(`the first number is greater than the second (${studentAge} > ${teacherAge})`);
} else {
    alert(`the second number is greater than the first (${studentAge} < ${teacherAge})`);
};



